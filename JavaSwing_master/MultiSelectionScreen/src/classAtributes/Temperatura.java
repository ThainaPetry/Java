package classAtributes;

public class Temperatura {
	private String tipoTemperaturaEntrada;
	private String tipoTemperturaSaida;
	private double valorEntrada;
	private double valorSaida;
	
	
	public String getTipoTemperaturaEntrada() {
		return tipoTemperaturaEntrada;
	}
	public void setTipoTemperaturaEntrada(String tipoTemperaturaEntrada) {
		this.tipoTemperaturaEntrada = tipoTemperaturaEntrada;
	}
	public String getTipoTemperturaSaida() {
		return tipoTemperturaSaida;
	}
	public void setTipoTemperturaSaida(String tipoTemperturaSaida) {
		this.tipoTemperturaSaida = tipoTemperturaSaida;
	}
	public double getValorEntrada() {
		return valorEntrada;
	}
	public void setValorEntrada(double valorEntrada) {
		this.valorEntrada = valorEntrada;
	}
	public double getValorSaida() {
		return valorSaida;
	}
	public void setValorSaida(double valorSaida) {
		this.valorSaida = valorSaida;
	}
}
