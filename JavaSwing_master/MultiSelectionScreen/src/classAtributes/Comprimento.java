package classAtributes;

public class Comprimento {
	private String tipoComprimentoEntrada;
	private String tipoComprimentoSaida;
	private double valorEntrada;
	private double valorSaida;
	
	
	public String getTipoComprimentoEntrada() {
		return tipoComprimentoEntrada;
	}
	public void setTipoComprimentoEntrada(String tipoComprimentoEntrada) {
		this.tipoComprimentoEntrada = tipoComprimentoEntrada;
	}
	public String getTipoComprimentoSaida() {
		return tipoComprimentoSaida;
	}
	public void setTipoComprimentoSaida(String tipoComprimentoSaida) {
		this.tipoComprimentoSaida = tipoComprimentoSaida;
	}
	public double getValorEntrada() {
		return valorEntrada;
	}
	public void setValorEntrada(double valorEntrada) {
		this.valorEntrada = valorEntrada;
	}
	public double getValorSaida() {
		return valorSaida;
	}
	public void setValorSaida(double valorSaida) {
		this.valorSaida = valorSaida;
	}
}
