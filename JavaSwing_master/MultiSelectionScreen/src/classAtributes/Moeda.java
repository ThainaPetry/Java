package classAtributes;

public class Moeda {
	private String tipoMoedaEntrada;
	private String tipoMoedaSaida;
	private double valorEntrada;
	private double valorSaida;
	
	
	public String getTipoMoedaEntrada() {
		return tipoMoedaEntrada;
	}
	public void setTipoMoedaEntrada(String tipoMoedaEntrada) {
		this.tipoMoedaEntrada = tipoMoedaEntrada;
	}
	public String getTipoMoedaSaida() {
		return tipoMoedaSaida;
	}
	public void setTipoMoedaSaida(String tipoMoedaSaida) {
		this.tipoMoedaSaida = tipoMoedaSaida;
	}
	public double getValorEntrada() {
		return valorEntrada;
	}
	public void setValorEntrada(double valorEntrada) {
		this.valorEntrada = valorEntrada;
	}
	public double getValorSaida() {
		return valorSaida;
	}
	public void setValorSaida(double valorSaida) {
		this.valorSaida = valorSaida;
	}
	
}
